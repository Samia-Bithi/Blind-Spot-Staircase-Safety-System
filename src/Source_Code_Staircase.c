/*
 * staircase_ir_2pir_buzzer_uart_ldr_atmega32a.c
 *
 * ATmega32A – Smart Staircase + LDR Auto-Brightness Ambient LED
 * ==============================================================
 *
 * Hardware connections
 * ?????????????????????
 *   IR Sensor 1 (Entry)   OUT -> PA0  (input, pull-up, active LOW)
 *   IR Sensor 2 (Exit)    OUT -> PA1  (input, pull-up, active LOW)
 *   LDR (voltage divider) OUT -> PA2  (ADC2 input)
 *
 *       VCC
 *        |
 *       LDR
 *        |
 *        +------> PA2 (ADC2)
 *        |
 *       10k (fixed resistor)
 *        |
 *       GND
 *
 *   Stair LED1                -> PD0  (output, entry side)
 *   Stair LED2                -> PC0  (output, exit side — moved from PD1)
 *   Ambient LED (PWM)         -> PD5  (OC1A, Timer1 Fast PWM 8-bit)
 *                                      220? resistor to GND
 *                                      ON at power-up, brightness auto-set by LDR
 *
 *   PIR Sensor 1 OUT          -> PD2  (INT0 input, no pull-up)
 *   PIR Sensor 2 OUT          -> PD3  (INT1 input, no pull-up)
 *   PIR1 motion LED           -> PB1  (output, 220? to GND)
 *   PIR2 motion LED           -> PB2  (output, 220? to GND)
 *   Buzzer                    -> PB0  (output, active HIGH)
 *   UART TX                   -> PD1  (to ESP32 GPIO16/RX2 via voltage divider)
 *
 * LDR Brightness Logic
 * ?????????????????????
 *   Bright light -> LDR resistance LOW  -> ADC reading HIGH -> LED brightness LOW
 *   Dark         -> LDR resistance HIGH -> ADC reading LOW  -> LED brightness HIGH
 *   (Matches the voltage divider wiring above — LDR on top, fixed resistor to GND)
 *
 * Staircase Behaviour
 * ?????????????????????
 *   IR1 fires ? LED1 ON, PIRs enabled, wait for IR2 exit
 *   IR2 fires ? LED2 ON, PIRs enabled, wait for IR1 exit
 *   Fall detected (10s no motion) ? buzzer ON
 *   Buzzer beeps 5s with no response ? send "FALL_DETECTED\n" via UART
 *   Normal exit via opposite IR     ? send "EXIT_OK\n" via UART
 *
 * Timers used
 * ?????????????
 *   Timer1 (16-bit) ? Fast PWM 8-bit on OC1A (PD5) for ambient LED brightness
 *   Timer0 is FREE
 *
 * Compile:
 *   avr-gcc -mmcu=atmega32 -DF_CPU=1000000UL -Os \
 *           -o staircase.elf staircase_ir_2pir_buzzer_uart_ldr_atmega32a.c
 *   avr-objcopy -O ihex staircase.elf staircase.hex
 *   avrdude -c usbasp -p m32 -U flash:w:staircase.hex
 */

#define F_CPU 1000000UL

#include <avr/io.h>
#include <avr/interrupt.h>
#include <util/delay.h>
#include <string.h>

/* ?? Pin definitions ???????????????????????????????????????????? */
#define IR1_BIT       PA0        /* Entry  IR sensor (active LOW)            */
#define IR2_BIT       PA1        /* Exit   IR sensor (active LOW)            */
/* PA2 = ADC2 = LDR input (no #define needed, used as channel number 2)     */

#define LED1_BIT      PD0        /* Stair LED 1 – entry side                 */
#define LED2_BIT      PC0        /* Stair LED 2 – exit side (moved from PD1) */
#define AMBIENT_BIT   PD5        /* OC1A – ambient PWM LED (always on)       */

#define PIR1_BIT      PD2        /* PIR Sensor 1 OUT – INT0                  */
#define PIR2_BIT      PD3        /* PIR Sensor 2 OUT – INT1                  */
#define PIR1_LED_BIT  PB1        /* PIR1 motion indicator LED                */
#define PIR2_LED_BIT  PB2        /* PIR2 motion indicator LED                */
#define BUZZER_BIT    PB0        /* Buzzer (active HIGH)                     */

/* ?? Tuning ????????????????????????????????????????????????????? */
#define HOLD_MS           3000   /* All LEDs on hold after exit (ms)         */
#define NO_MOTION_LIMIT   1000   /* 10 sec = 1000 × 10 ms poll ticks         */
#define FALL_ALERT_LIMIT   500   /* 5 sec  = 500  × 10 ms poll ticks         */
#define POLL_MS             10   /* Poll interval inside wait loop (ms)      */

#define BAUD              4800
#define UBRR_VAL          ((F_CPU / (16UL * BAUD)) - 1)

/* ?? LDR update rate inside staircase wait loop ???????????????? */
/* Every LDR_UPDATE_TICKS × POLL_MS ms the brightness is refreshed.
   At POLL_MS=10ms, 10 ticks = 100ms update rate — matches standalone code. */
#define LDR_UPDATE_TICKS    10

/* ?? Motion flags set by ISRs ?????????????????????????????????? */
volatile uint8_t motion1_flag = 0;
volatile uint8_t motion2_flag = 0;

/* ?? ISRs ?????????????????????????????????????????????????????? */
ISR(INT0_vect) { motion1_flag = 1; }
ISR(INT1_vect) { motion2_flag = 1; }

/* ????????????????????????????????????????????????????????????????
 *  ADC – LDR on PA2 (ADC2)
 * ???????????????????????????????????????????????????????????????? */
static void adc_init(void)
{
    /* AVCC as reference, channel selected at read time */
    ADMUX  = (1 << REFS0);
    /* Enable ADC, prescaler = 8  (1MHz / 8 = 125kHz — within 50–200kHz)  */
    ADCSRA = (1 << ADEN) | (1 << ADPS1) | (1 << ADPS0);
}

static uint16_t adc_read(uint8_t channel)
{
    channel &= 0x07;
    ADMUX   = (ADMUX & 0xF8) | channel;   /* Select channel, keep ref bits  */
    ADCSRA |= (1 << ADSC);                /* Start conversion               */
    while (ADCSRA & (1 << ADSC));         /* Wait for completion            */
    return ADC;                            /* 10-bit result (0–1023)         */
}

/* ????????????????????????????????????????????????????????????????
 *  Timer1 – Fast PWM 8-bit on OC1A (PD5) for ambient LED
 * ???????????????????????????????????????????????????????????????? */
static void pwm_init(void)
{
    DDRD   |= (1 << AMBIENT_BIT);               /* PD5 as output             */
    /* Fast PWM 8-bit (WGM10 + WGM12), non-inverting OC1A (COM1A1),
       prescaler = 8 ? PWM freq = 1MHz / (8 × 256) ? 488 Hz               */
    TCCR1A  = (1 << COM1A1) | (1 << WGM10);
    TCCR1B  = (1 << WGM12)  | (1 << CS11);
}

static void pwm_set_duty(uint8_t duty)
{
    OCR1A = duty;   /* 0 = LED off, 255 = full brightness                   */
}

/* ????????????????????????????????????????????????????????????????
 *  LDR brightness update — call this at ~100ms intervals
 * ???????????????????????????????????????????????????????????????? */
static void ldr_update_brightness(void)
{
    uint16_t ldr_val   = adc_read(2);            /* Read ADC2 (PA2)          */
    /* Bright  -> ADC high -> invert -> low brightness
       Dark    -> ADC low  -> invert -> high brightness                      */
    uint8_t  brightness = 255 - (uint8_t)(ldr_val >> 2);
    pwm_set_duty(brightness);
}

/* ????????????????????????????????????????????????????????????????
 *  UART
 * ???????????????????????????????????????????????????????????????? */
static void uart_init(void)
{
    UBRRH = (uint8_t)(UBRR_VAL >> 8);
    UBRRL = (uint8_t)(UBRR_VAL);
    UCSRB = (1 << TXEN);
    UCSRC = (1 << URSEL) | (1 << UCSZ1) | (1 << UCSZ0);   /* 8N1          */
}

static void uart_putc(char c)
{
    while (!(UCSRA & (1 << UDRE)));
    UDR = c;
}

static void uart_puts(const char *s)
{
    while (*s) uart_putc(*s++);
}

/* ????????????????????????????????????????????????????????????????
 *  PIR helpers
 * ???????????????????????????????????????????????????????????????? */
static inline void pir_enable(void)
{
    motion1_flag = 0;
    motion2_flag = 0;
    GIFR |= (1 << INTF0) | (1 << INTF1);
    GICR |= (1 << INT0)  | (1 << INT1);
}

static inline void pir_disable(void)
{
    GICR &= ~((1 << INT0) | (1 << INT1));
    motion1_flag = 0;
    motion2_flag = 0;
}

/* ????????????????????????????????????????????????????????????????
 *  Turn OFF staircase outputs (does NOT touch ambient PWM LED)
 * ???????????????????????????????????????????????????????????????? */
static inline void all_off(void)
{
    PORTD &= ~(1 << LED1_BIT);
    PORTC &= ~(1 << LED2_BIT);
    PORTB &= ~((1 << PIR1_LED_BIT) | (1 << PIR2_LED_BIT) | (1 << BUZZER_BIT));
}

/* ????????????????????????????????????????????????????????????????
 *  Wait for exit IR — with PIR monitoring, fall detection,
 *  UART alert, AND continuous LDR brightness updates
 * ???????????????????????????????????????????????????????????????? */
static void wait_for_exit_ir(uint8_t exit_bit)
{
    uint16_t no_motion_ticks = 0;
    uint16_t buzzer_ticks    = 0;
    uint8_t  fall_confirmed  = 0;
    uint8_t  ldr_tick_count  = 0;   /* Counts up to LDR_UPDATE_TICKS        */

    while (PINA & (1 << exit_bit))
    {
        uint8_t motion_detected = 0;

        /* ?? PIR checks ?? */
        if (motion1_flag)
        {
            motion1_flag    = 0;
            motion_detected = 1;
            PORTB |= (1 << PIR1_LED_BIT);
        }
        if (motion2_flag)
        {
            motion2_flag    = 0;
            motion_detected = 1;
            PORTB |= (1 << PIR2_LED_BIT);
        }

        /* ?? LDR brightness refresh every 100ms ?? */
        ldr_tick_count++;
        if (ldr_tick_count >= LDR_UPDATE_TICKS)
        {
            ldr_tick_count = 0;
            ldr_update_brightness();
        }

        /* ?? Fall detection logic ?? */
        if (motion_detected)
        {
            no_motion_ticks = 0;
            buzzer_ticks    = 0;
            fall_confirmed  = 0;
            PORTB &= ~(1 << BUZZER_BIT);
        }
        else
        {
            no_motion_ticks++;

            if (no_motion_ticks >= NO_MOTION_LIMIT)
            {
                PORTB |= (1 << BUZZER_BIT);
                no_motion_ticks = NO_MOTION_LIMIT;   /* Hold at limit        */

                if (!fall_confirmed)
                {
                    buzzer_ticks++;
                    if (buzzer_ticks >= FALL_ALERT_LIMIT)
                    {
                        uart_puts("FALL_DETECTED\n");
                        fall_confirmed = 1;
                        buzzer_ticks   = FALL_ALERT_LIMIT;
                    }
                }
            }
        }

        _delay_ms(POLL_MS);
    }

    uart_puts("EXIT_OK\n");
}

/* ????????????????????????????????????????????????????????????????
 *  Peripheral initialisation
 * ???????????????????????????????????????????????????????????????? */
static void init(void)
{
    /* ?? PA0, PA1 – IR inputs with pull-ups; PA2 – ADC (no pull-up) ?? */
    DDRA  &= ~((1 << IR1_BIT) | (1 << IR2_BIT) | (1 << PA2));
    PORTA |=  (1 << IR1_BIT)  | (1 << IR2_BIT);   /* PA2 pull-up stays OFF  */

    /* ?? PD0 – LED1, PD5 – ambient PWM LED (init'd by pwm_init) ?? */
    DDRD  |=  (1 << LED1_BIT);
    PORTD &= ~(1 << LED1_BIT);

    /* ?? PC0 – LED2 output ?? */
    DDRC  |=  (1 << LED2_BIT);
    PORTC &= ~(1 << LED2_BIT);

    /* ?? PD2, PD3 – PIR inputs, no pull-up ?? */
    DDRD  &= ~((1 << PIR1_BIT) | (1 << PIR2_BIT));
    PORTD &= ~((1 << PIR1_BIT) | (1 << PIR2_BIT));

    /* ?? PB0 – Buzzer, PB1/PB2 – PIR LEDs ?? */
    DDRB  |=  (1 << BUZZER_BIT) | (1 << PIR1_LED_BIT) | (1 << PIR2_LED_BIT);
    PORTB &= ~((1 << BUZZER_BIT) | (1 << PIR1_LED_BIT) | (1 << PIR2_LED_BIT));

    /* ?? INT0 & INT1 – rising edge ?? */
    MCUCR |= (1 << ISC01) | (1 << ISC00) | (1 << ISC11) | (1 << ISC10);
    GICR  &= ~((1 << INT0) | (1 << INT1));

    /* ?? Peripheral inits ?? */
    adc_init();
    pwm_init();
    uart_init();

    /* ?? Set initial brightness from LDR before main loop starts ?? */
    ldr_update_brightness();

    sei();
}

/* ????????????????????????????????????????????????????????????????
 *  Main
 * ???????????????????????????????????????????????????????????????? */
int main(void)
{
    uint8_t idle_ldr_tick = 0;

    init();

    while (1)
    {
        /* ?? FORWARD: IR1 fires first ????????????????????????? */
        if (!(PINA & (1 << IR1_BIT)))
        {
            PORTD |= (1 << LED1_BIT);
            pir_enable();

            wait_for_exit_ir(IR2_BIT);

            pir_disable();
            PORTB &= ~(1 << BUZZER_BIT);
            PORTC |=  (1 << LED2_BIT);

            _delay_ms(HOLD_MS);
            all_off();

            while (!(PINA & (1 << IR1_BIT)) || !(PINA & (1 << IR2_BIT)));
        }

        /* ?? REVERSE: IR2 fires first ????????????????????????? */
        else if (!(PINA & (1 << IR2_BIT)))
        {
            PORTC |=  (1 << LED2_BIT);
            pir_enable();

            wait_for_exit_ir(IR1_BIT);

            pir_disable();
            PORTB &= ~(1 << BUZZER_BIT);
            PORTD |=  (1 << LED1_BIT);

            _delay_ms(HOLD_MS);
            all_off();

            while (!(PINA & (1 << IR1_BIT)) || !(PINA & (1 << IR2_BIT)));
        }

        /* ?? IDLE: no IR triggered — keep updating LDR brightness ?? */
        else
        {
            _delay_ms(POLL_MS);
            idle_ldr_tick++;
            if (idle_ldr_tick >= LDR_UPDATE_TICKS)
            {
                idle_ldr_tick = 0;
                ldr_update_brightness();
            }
        }
    }

    return 0;
}